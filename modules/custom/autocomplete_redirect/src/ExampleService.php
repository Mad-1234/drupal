<?php

namespace Drupal\autocomplete_redirect;

/**
 * A simple service to perform arithmetic operations.
 */
class ExampleService {

  /**
   * Adds two numbers.
   *
   * @param int $a
   *   The first number.
   * @param int $b
   *   The second number.
   *
   * @return int
   *   The sum of the two numbers.
   */
  public function add($a, $b) {
    return $a + $b;
  }
}
